    private void flightSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_flightSubmitActionPerformed
        newFName = fNameField.getText();
        newFSeats = Integer.parseInt(fSeatsField.getText());
        if((newFName != null) && ((newFSeats > 0) || fSeatsField.getText() != null))
        {
            newFResult = Flights.addFlight(newFName, newFSeats);
            FlightArea.setText(newFResult);
            FlightArea.setVisible(true);
            FlightArea.setEditable(false);
            FComboBox.setModel(ComboBoxModel(Flights.getFlights()));
            FlightComboBox.setModel(ComboBoxModel(Flights.getFlights()));
            FSBox.setModel(ComboBoxModel(Flights.getFlights()));
        }
        else
        {
            newFResult = "Unable to add flight to database.";
            FlightArea.setText(newFResult);
            FlightArea.setVisible(true);
            FlightArea.setEditable(false);
        }
    }//GEN-LAST:event_flightSubmitActionPerformed

