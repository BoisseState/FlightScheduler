    private void deleteflightSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteflightSubmitActionPerformed
        if(deleteFName != null)
        {
            deleteFResult = Flights.dropFlight(deleteFName);
            FlightArea.setText(deleteFResult);
            FlightArea.setVisible(true);
            FlightArea.setEditable(false);
            FComboBox.setModel(ComboBoxModel(Flights.getFlights()));
            FlightComboBox.setModel(ComboBoxModel(Flights.getFlights()));
            FSBox.setModel(ComboBoxModel(Flights.getFlights()));
        }
        else
        {
            newFResult = "Unable to remove flight to database.";
            FlightArea.setText(newFResult);
            FlightArea.setVisible(true);
            FlightArea.setEditable(false);
        }
    }//GEN-LAST:event_deleteflightSubmitActionPerformed

