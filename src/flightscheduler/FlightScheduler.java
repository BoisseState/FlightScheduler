    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(Booking.seats(bookingFlight, bookingDate))
        {
            bookingResult = Booking.book(bookingCust, bookingFlight, bookingDate);
            BookingResults.setText(bookingResult);
            BookingResults.setVisible(true);
            BookingResults.setEditable(false);
        }
        else
        {
            String x = "We are sorry but there are no \nseats availabale for that flight.\n";
            String y = Waitlist.waitlist(bookingCust, bookingFlight, bookingDate);
            bookingResult = x + y;
            BookingResults.setText(bookingResult);
            BookingResults.setVisible(true);
            BookingResults.setEditable(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

